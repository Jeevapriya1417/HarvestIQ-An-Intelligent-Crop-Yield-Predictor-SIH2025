import mongoose from 'mongoose';

const predictionSchema = new mongoose.Schema({
  // User reference
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: [true, 'User is required'],
    index: true
  },

  // Field reference (optional, for field-specific predictions)
  field: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Field',
    default: null
  },

  // Input data from user
  inputData: {
    cropType: {
      type: String,
      required: [true, 'Crop type is required'],
      enum: ['Wheat', 'Rice', 'Sugarcane', 'Cotton', 'Maize', 'Barley', 'Mustard', 'Potato', 'Onion', 'Tomato']
    },
    farmArea: {
      type: Number,
      required: [true, 'Farm area is required'],
      min: [0.1, 'Farm area must be at least 0.1 hectares']
    },
    region: {
      type: String,
      required: [true, 'Region is required']
    },
    
    // Soil health data
    soilData: {
      phLevel: {
        type: Number,
        min: 0,
        max: 14
      },
      organicContent: {
        type: Number,
        min: 0,
        max: 100
      },
      nitrogen: {
        type: Number,
        min: 0
      },
      phosphorus: {
        type: Number,
        min: 0
      },
      potassium: {
        type: Number,
        min: 0
      }
    },

    // Weather data
    weatherData: {
      rainfall: {
        type: Number,
        min: 0
      },
      temperature: {
        type: Number
      },
      humidity: {
        type: Number,
        min: 0,
        max: 100
      }
    },

    // Additional parameters for advanced AI models
    additionalParams: {
      type: Map,
      of: mongoose.Schema.Types.Mixed,
      default: new Map()
    }
  },

  // AI Model information
  aiModel: {
    modelId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'AiModel',
      default: null
    },
    modelName: {
      type: String,
      default: 'JavaScript-Default'
    },
    modelVersion: {
      type: String,
      default: '1.0.0'
    },
    modelType: {
      type: String,
      enum: ['javascript', 'python-ml', 'python-dl', 'ensemble'],
      default: 'javascript'
    }
  },

  // Prediction results
  results: {
    expectedYield: {
      type: Number,
      required: [true, 'Expected yield is required'],
      min: 0
    },
    yieldPerHectare: {
      type: Number,
      required: [true, 'Yield per hectare is required'],
      min: 0
    },
    totalYield: {
      type: Number,
      required: [true, 'Total yield is required'],
      min: 0
    },
    confidence: {
      type: Number,
      min: 0,
      max: 100,
      default: 85
    },
    
    // Detailed factors affecting prediction
    factors: {
      weather: {
        type: String,
        enum: ['government-data', 'user-input', 'estimated'],
        default: 'user-input'
      },
      soil: {
        type: String,
        enum: ['government-data', 'user-input', 'estimated'],
        default: 'user-input'
      },
      yieldFactor: {
        type: Number,
        default: 1.0
      },
      additionalFactors: {
        type: Map,
        of: mongoose.Schema.Types.Mixed,
        default: new Map()
      }
    }
  },

  // Recommendations generated by AI
  recommendations: [{
    type: {
      type: String,
      enum: ['weather', 'irrigation', 'soil', 'nutrition', 'optimization', 'improvement'],
      required: true
    },
    priority: {
      type: String,
      enum: ['low', 'medium', 'high', 'critical'],
      default: 'medium'
    },
    title: {
      type: String,
      required: [true, 'Recommendation title is required']
    },
    description: {
      type: String,
      required: [true, 'Recommendation description is required']
    },
    action: {
      type: String,
      default: ''
    },
    estimatedImpact: {
      type: Number, // Percentage improvement expected
      min: 0,
      max: 100,
      default: 0
    }
  }],

  // Government data integration
  governmentData: {
    weather: {
      type: mongoose.Schema.Types.Mixed,
      default: null
    },
    soil: {
      type: mongoose.Schema.Types.Mixed,
      default: null
    },
    historical: {
      type: mongoose.Schema.Types.Mixed,
      default: null
    },
    market: {
      type: mongoose.Schema.Types.Mixed,
      default: null
    }
  },

  // Processing metadata
  processing: {
    status: {
      type: String,
      enum: ['pending', 'processing', 'completed', 'failed', 'cancelled'],
      default: 'completed'
    },
    processingTime: {
      type: Number, // Processing time in milliseconds
      default: 0
    },
    errorMessage: {
      type: String,
      default: null
    },
    retryCount: {
      type: Number,
      default: 0,
      max: 3
    }
  },

  // User interaction
  userFeedback: {
    rating: {
      type: Number,
      min: 1,
      max: 5,
      default: null
    },
    accuracy: {
      type: String,
      enum: ['very-low', 'low', 'medium', 'high', 'very-high'],
      default: null
    },
    comments: {
      type: String,
      default: ''
    },
    actualYield: {
      type: Number,
      min: 0,
      default: null
    }
  },

  // Metadata
  tags: [{
    type: String,
    trim: true
  }],
  
  notes: {
    type: String,
    maxLength: 1000,
    default: ''
  },

  isArchived: {
    type: Boolean,
    default: false
  },

  isPublic: {
    type: Boolean,
    default: false
  }

}, {
  timestamps: true,
  toJSON: { virtuals: true },
  toObject: { virtuals: true }
});

// Indexes for better query performance
predictionSchema.index({ user: 1, createdAt: -1 });
predictionSchema.index({ 'inputData.cropType': 1, 'inputData.region': 1 });
predictionSchema.index({ 'processing.status': 1 });
predictionSchema.index({ 'aiModel.modelType': 1 });
predictionSchema.index({ isArchived: 1 });
predictionSchema.index({ createdAt: -1 });

// Virtual for calculating accuracy if actual yield is provided
predictionSchema.virtual('accuracyPercentage').get(function() {
  if (this.userFeedback.actualYield && this.results.expectedYield) {
    const difference = Math.abs(this.results.expectedYield - this.userFeedback.actualYield);
    const accuracy = Math.max(0, 100 - (difference / this.results.expectedYield) * 100);
    return Math.round(accuracy * 100) / 100;
  }
  return null;
});

// Virtual for prediction age
predictionSchema.virtual('ageInDays').get(function() {
  return Math.floor((Date.now() - this.createdAt) / (1000 * 60 * 60 * 24));
});

// Instance methods
predictionSchema.methods.updateStatus = function(status, errorMessage = null) {
  this.processing.status = status;
  if (errorMessage) {
    this.processing.errorMessage = errorMessage;
  }
  return this.save();
};

predictionSchema.methods.addUserFeedback = function(feedback) {
  this.userFeedback = { ...this.userFeedback, ...feedback };
  return this.save();
};

predictionSchema.methods.archive = function() {
  this.isArchived = true;
  return this.save();
};

predictionSchema.methods.unarchive = function() {
  this.isArchived = false;
  return this.save();
};

// Static methods
predictionSchema.statics.findByUser = function(userId, options = {}) {
  const query = { user: userId, isArchived: false };
  
  if (options.cropType) {
    query['inputData.cropType'] = options.cropType;
  }
  
  if (options.region) {
    query['inputData.region'] = options.region;
  }
  
  if (options.modelType) {
    query['aiModel.modelType'] = options.modelType;
  }
  
  return this.find(query)
    .populate('user', 'fullName email')
    .populate('field', 'name location')
    .populate('aiModel', 'name version type')
    .sort({ createdAt: -1 });
};

predictionSchema.statics.getStatsByUser = function(userId) {
  return this.aggregate([
    { $match: { user: new mongoose.Types.ObjectId(userId), isArchived: false } },
    {
      $group: {
        _id: null,
        totalPredictions: { $sum: 1 },
        averageConfidence: { $avg: '$results.confidence' },
        averageYield: { $avg: '$results.expectedYield' },
        cropTypes: { $addToSet: '$inputData.cropType' },
        latestPrediction: { $max: '$createdAt' }
      }
    }
  ]);
};

// Pre-save middleware
predictionSchema.pre('save', function(next) {
  // Calculate total yield if not provided
  if (this.isModified('results.yieldPerHectare') || this.isModified('inputData.farmArea')) {
    this.results.totalYield = this.results.yieldPerHectare * this.inputData.farmArea;
  }
  
  next();
});

const Prediction = mongoose.model('Prediction', predictionSchema);

export default Prediction;